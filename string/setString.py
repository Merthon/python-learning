# 字符串格式设置：基本思想就是对字符串调用方法format,
# 并传入一个或多个参数，这些参数将会被用来替换字符串中的占位符。

# 举例：

# 假设我们有如下字符串：

string = "Hello, {0}! Your balance is {1:.2f}."

# 其中{0}表示第一个参数，{1}表示第二个参数，{1:.2f}表示第二个参数保留两位小数。

# 我们可以这样调用format方法：

result = string.format("Alice", 1000.5678)

# 结果为：

"Hello, Alice! Your balance is 1000.57."

# 这里，"Alice"被替换成了第一个参数"Alice"，1000.5678被替换成了第二个参数1000.5678，
# 并保留了两位小数。    